#include "libft.h"

int		ft_nod(int a, int b)
{
	if (a == 1)
		return (b);
	else if (b == 1)
		return (a);
	while (a != 0 && b != 0)
	{
		if (a > b)
			a %= b;
		else
			b %= a;
	}
	return (a + b);
}
